import docCategoryType from './docCategory'
import { BookIcon } from '@sanity/icons'
import { format, parseISO } from 'date-fns'
import { defineField, defineType } from 'sanity'
import {
  orderRankOrdering,
  orderRankField,
} from '@sanity/orderable-document-list'
/**
 * This file is the schema definition for a post.
 *
 * Here you'll be able to edit the different fields that appear when you 
 * create or edit a post in the studio.
 * 
 * Here you can see the different schema types that are available:

  https://www.sanity.io/docs/schema-types

 */

export default defineType({
  name: 'documentation',
  title: 'Documentation Article',
  icon: BookIcon,
  orderings: [
    {
      title: 'Rank',
      name: 'rank',
      by: [
        { field: 'orderRank', direction: 'asc' },
        { field: 'orderRank', direction: 'desc' },
      ],
    },
  ],
  type: 'document',
  fields: [
    defineField({
      name: 'title',
      title: 'Title',
      type: 'string',
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: 'description',
      title: 'Description',
      type: 'string',
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'title',
        maxLength: 96,
        isUnique: (value, context) => context.defaultIsUnique(value, context),
      },
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: 'content',
      title: 'Content',
      type: 'array',
      of: [{ type: 'block' }, {type: 'image'}],
    }),
    defineField({
      name: 'coverImage',
      title: 'Cover Image',
      type: 'image',
      options: {
        hotspot: true,
      },
    }),
    defineField({
      name: 'category',
      title: 'Category',
      type: 'reference',
      to: [{ type: docCategoryType.name }],
    }),
    orderRankField({ type: 'document' }),
  ],
  preview: {
    select: {
      title: 'title',
      category: 'category->title',
      media: 'coverImage',
    },
    prepare({ title, media, category }) {
      const subtitles = [category && `by ${category}`].filter(Boolean)

      return { title, media, subtitle: subtitles.join(' ') }
    },
  },
})
